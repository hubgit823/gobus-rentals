function buildCities(): CityRecord[] {
  const seen = new Set<string>();
  const out: CityRecord[] = [];
  for (const [state, names] of Object.entries(STATE_CITIES)) {
    for (const name of names) {
      const slug = slugify(name);
      if (!slug || seen.has(slug)) continue;
      seen.add(slug);
      out.push({ slug, name, state });
    }
  }
  return out.sort((a, b) => a.name.localeCompare(b.name));
}

export const INDIAN_CITIES: CityRecord[] = buildCities();

const cityMap = new Map(INDIAN_CITIES.map((c) => [c.slug, c]));

export function getCityBySlug(slug: string): CityRecord | undefined {
  return cityMap.get(slug);
}

export function getRelatedCities(city: CityRecord, limit = 8): CityRecord[] {
  const sameState = INDIAN_CITIES.filter((c) => c.state === city.state && c.slug !== city.slug);
  const rest = INDIAN_CITIES.filter((c) => c.state !== city.state && c.slug !== city.slug);
  const merged = [...sameState, ...rest];
  return merged.slice(0, limit);
}
